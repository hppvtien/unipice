                                                <h3><a href="#product">Product</a>&nbsp;&nbsp;| &nbsp;<a href="#organic">Organic</a>&nbsp;&nbsp;| &nbsp;<a href="#quality">Quality</a>&nbsp;&nbsp;| &nbsp;<a href="#safety">Safety</a></h3>

                                                <p>&nbsp;</p>

                                                <p>If you need help navigating or ordering from this site, you can call Online Customer Assistance during business hours (7am - 6pm CT, Monday - Friday) at 800-669-3275. We've answered some of the most frequently asked questions below. If you don't see the information you're looking for, you can email&nbsp;<a href="mailto:customercare@frontiercoop.com">Customer Care</a>.</p>

                                                <p>&nbsp;</p>

                                                <h2><a id="product"></a>Product</h2>

                                                <h2>&nbsp;</h2>

                                                <p><strong>Q: What allergens are present in your facility?</strong><br>
                                                    A: We manufacture some products within our facility with ingredients that contain allergens. When ingredients with allergens first arrive at our facility, we are careful to identify them, tag them and isolate them in storage. When we process products that have any of these ingredients, we do a special cleanup afterwards to prevent the allergens from carrying over to subsequent processing. We provide full disclosure of all ingredients on our labels, so you can check there for any allergens you wish to avoid.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What is your policy on animal testing?</strong><br>
                                                    A: None of our products are tested on animals.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: How much caffeine is in your tea?</strong><br>
                                                    A: The caffeine content of tea varies depending on a number of factors, including the variety and age of the leaves, the size of the leaves, the environment where they were grown, and the method of steeping. Generally speaking, the longer a tea is steeped, the higher its caffeine content. Because black tea is often steeped longer than green or white, it's considered to have the most caffeine â€” even though the dry leaves of black teas aren't higher in caffeine than those of green teas. The general range is 15 to 70 mg of caffeine per cup.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Q: Is your facility gluten-free?</strong><br>
                                                    A: While spices and herbs are naturally gluten-free, we do not test for gluten in all products, only those certified gluten-free (by GFCO) under the Simply Organic brand. We don't make gluten-free claims for any other products because even tiny amounts of gluten can be a problem, and these may be present in our facility or the facilities of our suppliers.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Do your products contain any genetically modified ingredients (GMOs)?</strong><br>
                                                    A: We no longer receive or produce any products in our Food and Flavorings division with GMO ingredients or GMO-suspect ingredients, and all finished goods are free of GMO ingredients and suspected GMO ingredients.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Are your products irradiated?</strong><br>
                                                    A: We have always opposed using irradiation for sterilization of spices, herbs and foods, and we do not use any ingredients or carry any products that are irradiated under any of our three brands (Frontier, Simply Organic, and Aura Cacia).</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What does kosher certified mean?</strong><br>
                                                    A: In order for a product to be kosher certified, a kosher certification company must inspect the production process from start to finish, checking every container and all conveying, processing and packaging machinery to ensure that nothing non-kosher can get into the food.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Who is your kosher certifier?</strong><br>
                                                    A: Frontier and Simply Organic products are certified by <a href="http://www.ksakosher.com/">Kosher Supervision of America (KSA)</a>.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Are your products kosher?</strong><br>
                                                    A: Most Frontier and Simply Organic spices and seasonings are kosher. This information is on product labels and is also accessible online on each item information page.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Why don't you provide any information about the medicinal uses of herbs?</strong><br>
                                                    A: In a nutshell, because it's against the law. There are strict Federal regulations prohibiting any herb supplier from making health claims for their products. Our practices comply with those FDA and FTC regulations and follow the recommendations of the American Herbal Products Association.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Do your products contain MSG?</strong><br>
                                                    A: We do not add monosodium glutamate (MSG) to any of our products. FDA regulations require that if MSG is added to a product it must be identified as monosodium glutamate on the label. Some of our ingredients may contain some naturally occurring glutamic acid, which can combine with sodium present in the product to naturally form MSG. While the FDA classifies MSG and glutamic acid as generally safe, people who are sensitive to MSG and glutamic acid may suffer from adverse reactions.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Where can I find nutritional information for your products?</strong><br>
                                                    A: All of the food products and dietary supplements that we sell that have reportable nutrients (amounts significant enough to warrant reporting according to FDA food labeling regulations) will feature a Nutrition Facts Panel or Supplement Facts Panel on each item information web page. (See the <a href="#reportable-nutrients">reportablenutrients</a> FAQ for more info.)</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Why isnâ€™t there nutritional information on most spices?</strong><br>
                                                    A: Most of our single ingredient spices (that is, basil, fennel, cumin, etc.) don't contain any reportable nutrients. Reportable nutrients are amounts significant enough to warrant reporting according to FDA food labeling regulations. (See the <a href="#reportable-nutrients">reportable nutrients</a> FAQ for more info.)</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: How is your nutritional yeast produced?</strong><br>
                                                    A: It's a primary-grown yeast from pure strains of <em>Saccharomyces cerevisiae</em> grown on mixtures of cane and beet molasses. After the fermentation process is completed, the yeast is harvested, thoroughly washed, pasteurized, and dried on roller drum dryers. Vitamins B2 and B12 are both naturally produced during fermentation. Additional B2 and B12 are added after fermentation but prior to the drying process, along with B1, B6, niacin and folic acid. Due to the naturally occurring color variation of the molasses culture used to grow this yeast, some color variation may be present in the finished product. Note: Glutamic acid is present in this product.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: How are your products processed?</strong><br>
                                                    A: We do not regulate the methods our suppliers use to grow, harvest, and process the products we purchase from them. Processing may vary from crop to crop and amongst suppliers. The way in which suppliers process their crops is often considered proprietary. They determine the best method to produce a high-quality product. Our Quality Assurance department has researched and developed a set of specifications for each product that we offer, and we ensure all products meet those specs with a comprehensive <a href="/community/about/stewardship/quality">quality program</a>.</p>

                                                <p>&nbsp;</p>

                                                <p><strong><a id="reportable-nutrients"></a>Q: What are reportable nutrients?</strong><br>
                                                    A: FDA food labeling regulations (21CFR101) exempt nutrient listings for foods that contain insignificant amounts of all of the nutrients and food components in the standard declaration of nutrition information. (An insignificant amount of a nutrient is defined as the amount that would show a zero in the nutrition labeling, or "less than 1 gram'' in the case of total carbohydrate, dietary fiber, and protein.) Since the nutrient levels are based on the amount customarily consumed by a person at one "eating occasion" (defined by the FDA as 1/4 teaspoon), most spices do not meet the threshold of having reportable nutrients.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: How can I tell if my herbs and spices are still good?</strong><br>
                                                    A: The potency of botanical products fades over time at different rates. Grinding exposes more surface area to the air, so ground spices lose their freshness more quickly than whole spices. As you can see in the table below, different parts of the plant age differently as well.<br><br>
                                                    Our bottled spices and blends are freshness dated â€” the date the product is best used by for maximum freshness is printed on the bottom of each bottle. Check that date for unopened products. For opened packaged products and bulk products, follow the guidelines below:<br><br>
                                                    Whole herbs, spices and seasoning blends<br>
                                                    Leaves and flowers â€” 1 to 2 years<br>
                                                    Seeds and barks â€” 2 to 3 years<br>
                                                    Roots â€” 3 years<br>
                                                    Vanilla beans â€” 2 years<br>
                                                    Others â€” 2 to 3 years<br><br>
                                                    Ground herbs, spices and seasoning blends<br>
                                                    Leaves â€” 1 year<br>
                                                    Seeds and barks â€” 1 year<br>
                                                    Roots â€” 2 years</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: How long do teas stay good?</strong><br>
                                                    A: Teas stored in a cool, dry, airtight, opaque container should be good for up to a year.</p>

