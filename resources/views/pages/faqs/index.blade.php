                                                <h2><a id="quality"></a>Essential Oil&nbsp;Quality</h2>

                                                <h2>&nbsp;</h2>

                                                <p><strong>Q: What are the most common solvents used to adulterate essential oils? Do they pose any health concerns?</strong>&nbsp;<br>
                                                    A: We test extensively to ensure that Aura Cacia essential oils are pure and unadulterated. Solvents used to adulterate essential oils are generally colorless and odorless and therefore not easily detectable without resorting to gas chromatography testing. These solvents range from relatively benign (ethanol) to very hazardous (toluene) chemicals. Regardless of individual health effects, they do not belong in essential oils. They could affect the benefits of the oils, and they may cause allergic or toxic reactions in some individuals.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Do you test all Aura Cacia essential oils?</strong>&nbsp;<br>
                                                    A: We test every pre-shipment sample of essential oils before making a purchase. Then, after we purchase an essential oil, every shipment is tested to make sure it continues to meet all quality and purity standards.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What specific tests do you perform for essential oils?</strong>&nbsp;<br>
                                                    A: Our essential oil testing includes both physical testing â€” sensory evaluation, optical rotation and specific gravity â€” and gas chromatography (GC) testing for every oil. This extensive testing provides complete confidence that our oils are 100% pure and authentic, and we offer a quality guarantee to our customers.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What is a therapeutic grade oil?</strong>&nbsp;<br>
                                                    A: There is no such grading system for essential oils. Companies sometimes use this term in marketing to try to differentiate aromatherapy oils from essential oils used in flavoring and food. Aromatherapy oils should be pure, complete and unadulterated high-quality oils. But because the term "therapeutic grade" isn't based on established standards or regulation, it can be â€” and often is â€” used without regard to the quality of the oil.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What is the difference between a fragrance oil and an essential oil?</strong>&nbsp;<br>
                                                    A: Essential oils are distilled from the leaves, roots, flowers, seeds, tops, or fruits of plants. They are the highly concentrated, volatile, aromatic components of the plants or plant parts from which they are distilled. A fragrance is a combination of various natural and synthetic aroma chemicals, created in the lab by an aroma chemist. Fragrances attempt to mimic the aroma of an essential oil or the aroma of a plant that doesn't have an essential oil (an apple for example), or to create an entirely new scent. Fragrances are used in perfumery and to scent all types of products, such as toiletries, soaps, room fresheners, paper, tobacco products and paints. Only pure essential oils should be used in aromatherapy.&nbsp;</p>

                                                <p>&nbsp;</p>

                                                <h2><a id="safety"></a>Essential Oil&nbsp;Safety</h2>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Where are the best places to apply oils for absorption?</strong><br>
                                                    A: Essential oils can be applied almost anywhere on the body (avoid the eyes, of course). However, the fastest absorption occurs in areas where you have lots of blood vessels close to the surface of the skin. (Many of these are also called pressure points.) The soles of the feet, the wrists, neck and behind the ears are all great spots for applying oils to improve absorption. Warmth also plays a role â€” your body doesn't absorb oils as well when it's cold. The underarm is also an ideal place to apply oils; it has lots of blood vessels and is also warm.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Can you put undiluted oils directly on your skin?</strong><br>
                                                    A: In general, this should be avoided, and oils should only be used with a recommended dilution based on the particular situation of the person and the intended use of the oil (from 1-10%). A few oils, like lavender and tea tree, can occasionally be applied directly, but in general, dilution is always recommended. With any oil we recommend a skin patch test. Always use caution.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Can you use oils in food products?</strong><br>
                                                    A: We don't recommend using essential oils for flavoring food at home due to safety concerns.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Why doesnâ€™t Aura Cacia have health benefits on their labels?</strong><br>
                                                    A: Aromatherapy products are regulated by the FDA as cosmetics and must conform to cosmetic labeling regulations. Aura Cacia labels all products in compliance with regulations in order to protect users. Not all companies comply with the law â€” that's why you may see competing products making claims or statements about the product's supposed benefits that you won't see on an Aura Cacia label.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What causes photosensitivity and what oils cause it?</strong><br>
                                                    A: Oils that contain furocoumarins can cause skin reactions if used on skin that is then exposed to ultraviolet light. These photosensitive reactions may be as mild as slight reddening of the skin, while severe cases can result in acute lesions known as bullock dermatitis. This dermatitis will resolve itself in a few weeks; however, the accompanying hyper-pigmentation (brown spots on the skin) can take months or years to fully disappear. Bergamot, which contains the furocoumarin bergaptene, is especially likely to cause reactions. We sell bergaptene-free bergamot (bergamot BF) to eliminate this risk. Other oils we carry that might cause photosensitive reactions include ginger, grapefruit, lemon, lime, mandarin orange and tangerine.</p>
