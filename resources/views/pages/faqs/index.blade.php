                                                <h2><a id="organic"></a>Organic</h2>

                                                <h2>&nbsp;</h2>

                                                <p><strong>Q: What does "certified organic" mean?</strong><br>
                                                    A: "Certified organic" is a labeling term that denotes products produced under the authority of the Organic Foods Production Act. The act provides for a National Organic Standards Board (NOSB) that develops and recommends the standards for the National Organic Program (NOP) as administered by the United States Department of Agriculture (USDA). A certified organic designation means that a state or private certification organization that is accredited by the USDA has verified that the product meets its strict organic standards. The certifier inspects the location where the organic product is produced and handled to ensure that all the rules necessary to meet USDA organic standards are being followed. Companies that handle or process organic food before it gets to the local supermarket or restaurant must also be certified and inspected annually to ensure continued compliance. (In order to maintain their accredited certifier status with the USDA, certifiers are required to conduct annual on-site inspections of all their clients.)</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Who is your organic certifier?</strong><br>
                                                    A: Frontier, Simply Organic and Aura Cacia organic products are all certified by the leading third-party organic certifier <a href="http://www.qai-inc.com/">Quality Assurance International (QAI)</a>.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Are organic products produced in other countries required to meet U.S. organic regulations?</strong><br>
                                                    A: Yes, all organic products sold as certified organic in the United States are required to follow the U.S. standards and be certified by a USDA-accredited certifier.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: What is organic agriculture?</strong><br>
                                                    A: In a nutshell, organic farming is the form of agriculture that relies on techniques such as crop rotation, green manure, compost and biological pest control to maintain soil productivity and control pests on a farm. Or in the words of the National Organics Standard Board, "Organic agriculture is an ecological production management system that promotes and enhances biodiversity, biological cycles and soil biological activity. It is based on minimal use of off-farm inputs and on management practices that restore, maintain and enhance ecological harmony." More definitions and extensive resource links are available at the USDA's Organic Production and Organic Food: Information Access Tools. Further online resources on organics, sustainability, farm energy and alternative crops (including <a href="http://www.nal.usda.gov/ref/herbs.html">herbs</a>) can be found in the list of Alternative Farming Systems Information Center (AFSIC) publications.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Can you help me understand certain organic claims? For example, what is the difference between 100% Organic vs. Made with Organic?</strong><br>
                                                    A: Here are the basic USDA regulations for the four recognized types of organic claims: Products labeled "100 Percent Organic" must show an ingredients list, the name and address of the handler (bottler, distributor, importer, manufacturer, packer, processor, etc.) of the finished product, and the name/seal of the organic certifier. All ingredients must be certified organic (excluding water and salt). Products labeled "Organic" must contain at least 95 percent organic ingredients. The label must contain an ingredient list that identifies the organic, as well as the non-organic, ingredients in the product and the name of the organic certifier. A minimum of 95% of the ingredients (excluding water and salt) must be certified organic, and any non-organic ingredients used must be approved for use in an organic product by the USDA. Products labeled "Made with Organic (specified ingredients or food groups)" must contain at least 70 percent organic ingredients (excluding water and salt) and up to 30% non-organic agricultural ingredients or other ingredients approved for use in organic products by the USDA. The label must contain an ingredients list that identifies the organic, as well as the non-organic, ingredients in the product, along with the name of the organic certifier. If a product contains less than 70 percent organic ingredients, the product can specify organic ingredients only on the ingredient panel. The product cannot use the word "organic" on the principal display panel or display any organic certifier seals.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Are organic foods more expensive than conventionally grown ones?</strong><br>
                                                    A: It's true that organic foods that have the same growing, harvesting, transportation and storage costs as conventional items usually have additional costs associated with stricter growing regulations of organic certification and smaller-scale production that contribute to higher prices. This was dramatically the case when organics first came on the market. But the price gap is continually shrinking as increased demand for organics and a more robust organic supply chain are driving down the cost of organic foods, making them much more competitive with non-organic ones. Furthermore, many argue that from the perspective of the <em>true</em> cost of our food â€” factoring in all the indirect environmental and social costs of conventional food production, such as cleanup of polluted water, replacement of eroded soils, and costs of health care for farmers and their workers â€” organic foods are already less costly than those grown with synthetic chemicals.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Is organic food healthier?</strong><br>
                                                    A: Organic farming techniques provide a safer, more sustainable environment for everyone â€” growers, consumers and all of us who share the world environment. There is substantial research suggesting links to cancer and other diseases from the synthetic insecticides, herbicides, fungicides and fertilizers used on non-organic crops. And the <a href="http://www.organic-center.org/">Organic Center</a> provides links to numerous studies that suggest some organically produced foods may be more nutritious than non-organic ones as well.</p>

                                                <p>&nbsp;</p>

                                                <p><strong>Q: Are organic products completely free of pesticide residues?</strong><br>
                                                    A: The 1995 definition of organic production by the National Organics Standard Board notes that "Organic agriculture practices cannot ensure that products are completely free of residues; however, methods are used to minimize pollution from air, soil and waters." These methods include buffer zones between conventional and organic fields, a three-year waiting period before previously non-organic land can be used for organic crops and placing organic products in storage on the higher shelves to avoid cross-contamination from non-organic products. Products are tested by certification agencies for contamination in response to a complaint, to spot-check certain crops, or if there is any evidence of contamination. The principal guidelines for organic production require using materials and practices that enhance the ecological balance of natural systems and integrate farming into the whole ecology.&nbsp;</p>

